"""
    函数参数
        实际参数：调用者以何种方式传递数据
            位置实参
                -- 序列实参
            关键字实参
                -- 字典实参
"""


def func01(p1, p2, p3):
    print(p1)
    print(p2)
    print(p3)


# 1. 位置实参：位置
func01(1, 2, 3)

list01 = ["a", "b", "c"]
# 2.   -- 序列实参：拆
func01(*list01)

# dict01 = {"悟空":2,"唐三藏":3,"春光灿烂猪八戒":7}
# func01(*dict01)# 之传入key,所以没有意义

# 3. 关键字实参:名字
func01(p3=2, p1=1, p2=3)

# 4.  -- 字典实参：拆
dict01 = {"p3": "c", "p1": 1, "p2": "b"}
func01(**dict01)
